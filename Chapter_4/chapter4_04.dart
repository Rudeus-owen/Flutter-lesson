/* Difference between Expresssion and Token 

Token

A token is the smallest unit in Dart code.It can be
 . Keywords: if , for , return , class , etc 
 . Identifiers: Names of variables, functions, classes(eg.,myVar , print)
 . Literals:Numbers, strings, booleans( 10 , "hello",true)
 . Operators: +,- , * , = , == , etc
 . Punctuation: ;, ,, (, ), {, }

Expression

An epression is a combination of tokens that produces a vale.
Types of expressions:
 . Literal : 42 , 'hello'
 . Arithmetic: 5 + 3 , a * b
 . Relational: a > b, x == y
 . function call : print('Hi')
 . Conditional (ternary): condition ? a : b

